
let num1 = 10;
let num2 = 5;

const operator = '*';

if (operator === '+') {
    console.log(num1 + num2);
}
else if (operator === '-') {
    console.log(num1 - num2);
}
else if (operator === '*') {
    console.log(num1 * num2);
}
else if (operator = '/') {
    console.log(num1 / num2);
}
else {
    console.log("Invalid operator");
}
// Output: 50